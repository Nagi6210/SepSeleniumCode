package Strings;

public class FirstLastCharString {

	public static void main(String[] args) {
		String str="Hello World";
		System.out.println("First Char of string : \'"+str+"\' is : "+str.charAt(0));
		System.out.println("Last  Char of string : \'"+str+"\' is : "+str.charAt(str.length()-1));

	}

}
